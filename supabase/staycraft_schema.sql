update public.room_bookings
set
  booking_status = case
    when status = 'paid' then 'paid'
    else 'initiated'
  end,
  payment_status = status,
  owner_id = coalesce(owner_id, (select owner_id from public.rooms where public.rooms.id = public.room_bookings.room_id))
where booking_status is null
   or payment_status is null
   or owner_id is null;

