insert into public.profiles (id, email, role, is_active)
select
  id,
  email,
  case
    when raw_user_meta_data ->> 'preferred_role' in ('admin', 'owner', 'tenant')
      then raw_user_meta_data ->> 'preferred_role'
    else 'tenant'
  end,
  true
from auth.users
on conflict (id) do update
set
  email = excluded.email,
  role = excluded.role,
  updated_at = timezone('utc', now());

create table if not exists public.rooms (
  id integer primary key generated by default as identity,
  owner_id uuid references public.profiles(id) on delete set null,
  image text not null,
  cover_image_url text,
  price text not null,
  monthly_rent_paise integer check (monthly_rent_paise is null or monthly_rent_paise > 0),
  security_deposit_paise integer check (security_deposit_paise is null or security_deposit_paise >= 0),
