create or replace function public.is_admin(user_id uuid default auth.uid())
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select exists (
    select 1
    from public.profiles
    where id = user_id
      and role = 'admin'
      and is_active = true
  );
$$;

create or replace function public.handle_new_auth_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  requested_role text;
begin
  requested_role := coalesce(new.raw_user_meta_data ->> 'preferred_role', 'tenant');

  if requested_role not in ('admin', 'owner', 'tenant') then
    requested_role := 'tenant';
  end if;

  insert into public.profiles (id, email, role, is_active)
  values (new.id, new.email, requested_role, true)
  on conflict (id) do update
  set
    email = excluded.email,
    role = excluded.role,
    updated_at = timezone('utc', now());

  return new;
end;
$$;

